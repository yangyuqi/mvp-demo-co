package com.wxjz.module_aliyun.aliyun.view.more;

public class AliyunShowMoreValue {
    private int volume;
    private int screenBrightness;
    private float speed;

    private boolean isStudyStatus;

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }

    public int getScreenBrightness() {
        return screenBrightness;
    }

    public void setScreenBrightness(int screenBrightness) {
        this.screenBrightness = screenBrightness;
    }

    public float getSpeed() {
        return speed;
    }

    public void setSpeed(float speed) {
        this.speed = speed;
    }


    public boolean isStudyStatus() {
        return isStudyStatus;
    }

    public void setStudyStatus(boolean studyStatus) {
        isStudyStatus = studyStatus;
    }
}
